int ft_atoi(char *str);

int ft_atoi(char *str)
{
	char val;
	int res;
	int i;

	i = 0;
	while (*(str + i) != '\0')
	{
		if (i != 0)
		{
			res *= 10;
		}
		val = *(str+i) - '0';
		res += (int)val;
		i++;
	}
	return res;
}
