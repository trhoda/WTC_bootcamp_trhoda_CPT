int ft_str_is_lowercase(char *str);

int ft_str_is_lowercase(char *str)
{
	int i;
	i = 0;
	char c;

	while (str[i] != '\0')
	{
		c = str[i++];
		if (c < 'a' || c > 'z')
		{
			return 0;
		}
	}
	return 1;
}
